# Календарный месяц → русское название (fallback, если дата не попала ни в одну
# неделю из MONTH_WEEK_RANGES — например, дата за пределами 2025-2027)
_CAL_MONTH = {1:'Январь',2:'Февраль',3:'Март',4:'Апрель',5:'Май',6:'Июнь',
              7:'Июль',8:'Август',9:'Сентябрь',10:'Октябрь',11:'Ноябрь',12:'Декабрь'}

def month_for_date(date_val, default: str = 'Май') -> str:
    """Возвращает русское название месяца по дате операции — по правилу
    границ недель (MONTH_WEEK_RANGES), а не по календарному месяцу даты.
    Значит дата 1-2 августа попадёт в 'Июль' (5-я неделя июля), а не в 'Август'."""
    try:
        d = pd.Timestamp(date_val)
        for month, ranges in MONTH_WEEK_RANGES.items():
            for (y1,m1,d1,y2,m2,d2) in ranges:
                start = pd.Timestamp(year=y1, month=m1, day=d1)
                end   = pd.Timestamp(year=y2, month=m2, day=d2)
                if start <= d <= end:
                    return month
        if d.month in _CAL_MONTH:
            return _CAL_MONTH[d.month]
    except Exception:
        pass
    return default

def _classify(contractor: str, description: str, db_map: dict = None) -> str:
    c = ' '.join((contractor or '').lower().strip().split())
    d = (description or '').lower()
    # Сначала проверяем пользовательский справочник из БД
    if db_map and c in db_map:
        return db_map[c]
    if c in MANUAL_MAP:
        return MANUAL_MAP[c]
    for cat, pattern in KEYWORDS:
        if re.search(pattern, d):
            return cat
    return 'Прочие нераспознанные'

def _find_col(df: pd.DataFrame, *candidates) -> str | None:
    for col in df.columns:
        for cand in candidates:
            if cand in col.lower():
                return col
    return None

def _get_week_index(date_val, month: str) -> int:
    try:
        d = pd.Timestamp(date_val)
        for i, (y1,m1,d1,y2,m2,d2) in enumerate(MONTH_WEEK_RANGES.get(month, [])):
            start = pd.Timestamp(year=y1, month=m1, day=d1)
            end   = pd.Timestamp(year=y2, month=m2, day=d2)
            if start <= d <= end:
                return i
    except Exception:
        pass
    return -1

def classify_operations(df: pd.DataFrame, month: str) -> dict:
    date_col   = _find_col(df, 'дата')
    type_col   = _find_col(df, 'тип')
    contr_col  = _find_col(df, 'контраг', 'получат', 'плательщ')
    debit_col  = _find_col(df, 'дебет', 'расход', 'списан')
    credit_col = _find_col(df, 'кредит', 'приход', 'зачис')
    desc_col   = _find_col(df, 'назначен', 'описани', 'основани')

    weeks_out  = [0.0] * 5
    weeks_in   = [0.0] * 5
    cat_totals: dict[str, float] = {}
    ops        = []
    unknown    = []

    db_map = get_contractor_mappings()
    for _, row in df.iterrows():
        try:
            raw_debit  = float(row.get(debit_col,  0) or 0) if debit_col  else 0.0
            raw_credit = float(row.get(credit_col, 0) or 0) if credit_col else 0.0

            if type_col:
                type_str = str(row.get(type_col, '')).lower()
                is_debit = bool(re.search(r'дебет|списан|расход', type_str))
            elif debit_col and credit_col:
                if raw_debit > 0 and raw_credit == 0:
                    is_debit = True
                elif raw_credit > 0 and raw_debit == 0:
                    is_debit = False
                elif raw_debit > 0:
                    is_debit = True
                else:
                    continue
            else:
                is_debit = True

            amt = raw_debit if is_debit else raw_credit
            if not (amt > 0):
                continue

            raw_contr  = row.get(contr_col, '')
            contractor = '' if (raw_contr is None or str(raw_contr).strip().lower() == 'nan') else str(raw_contr)
            # Заменяем ИНН на читаемое имя контрагента
            contractor = INN_TO_NAME.get(contractor.strip(), contractor)
            desc       = str(row.get(desc_col,  '') or '')
            date_val   = row.get(date_col)
            # Кредитные операции — всегда "Поступления от клиентов"
            if not is_debit:
                cat = 'Поступления от клиентов'
            else:
                cat = _classify(contractor, desc, db_map)
            wi         = _get_week_index(date_val, month)

            try:
                d = pd.Timestamp(date_val)
                date_str = d.strftime('%d.%m.%Y')
            except Exception:
                date_str = str(date_val)[:10]

            if is_debit:
                if 0 <= wi < 5:
                    weeks_out[wi] += amt
                cat_totals[cat] = cat_totals.get(cat, 0.0) + amt
                if cat == 'Прочее':
                    unknown.append({'contractor': contractor[:80], 'desc': desc[:80], 'amount': round(amt)})
            else:
                if 0 <= wi < 5:
                    weeks_in[wi] += amt
                cat_totals[cat] = cat_totals.get(cat, 0.0) + amt

            ops.append({
                'date': date_str, 'contractor': contractor[:60],
                'desc': desc[:80], 'amount': round(amt),
                'cat': cat, 'week': wi, 'is_debit': is_debit,
            })
        except Exception:
            continue

    cats = [{'name': k, 'fact': round(v)} for k, v in sorted(cat_totals.items(), key=lambda x: -x[1])]

    return {
        'month': month,
        'weeks_out': [round(x) for x in weeks_out],
        'weeks_in':  [round(x) for x in weeks_in],
        'cats': cats,
        'ops':  ops,
        'total_ops': len(ops),
        'unknown': unknown,
        'uploaded_at': datetime.now().isoformat(),
        'source': 'manual',
    }

